class Stringdemo
{
	public static void main(String args[])
	{
		String str="wel come";
		System.out.println("Length of String ="+str.length());
		System.out.println("Index of e ="+str.indexOf('e'));
		System.out.println("Index of e ="+str.indexOf('e',3));
		System.out.println("Last Index of e ="+str.lastIndexOf('e'));
		System.out.println("Char at="+str.charAt(4));
		System.out.println("Uppere Case="+str.toUpperCase());
		System.out.println("Lower Case="+str.toLowerCase());
	}
}
